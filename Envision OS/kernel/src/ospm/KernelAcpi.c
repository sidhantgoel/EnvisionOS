#include <KernelACPI.h>

BOOL AcpiEnabled=FALSE;

KESTATUS KeACPIInit()
{
	KESTATUS status;
	status = KeAcpiTbInit();
    if(KESTATUS_ERROR(status))
	{
		AcpiEnabled=FALSE;
		return status;
	}
    return ACPI_OK;
}

KESTATUS KeAcpiStatus()
{
	if(AcpiEnabled)
	{
		return ACPI_ENABLED;
	}
	return ACPI_DISABLED;
}

