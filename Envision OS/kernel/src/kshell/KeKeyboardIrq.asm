.586
.model flat

public _KeKeyboardIrq
extern _KbdKbd:proc

.code _TEXT

_KeKeyboardIrq:
  
    cli
    pushad
    push ds
    push es
    push fs
    push gs
    call _KbdKbd
    pop gs
    pop fs
    pop es
    pop ds
    mov dx,20h
    mov al,20h
    out dx,al
    popad
    iretd

_TEXT ends
end

